int color(int x)
{
    if (x==9) return 255;
    else if (x==11) return 217;
    else if (x==6) return 94;
    else if (x==1) return 31;
    else if (x==7) return 9;
    else if (x==12) return 223;
    else if (x==5) return 11;
    else return 231;
}

